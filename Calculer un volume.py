#Calculer le volume d'un parallélépipède

Longueur = float(input("Veuiller saisir un nombre(en cm) : "))
Largeur = float(input("Veuiller saisir un nombre (en cm) : "))
Hauteur = float(input("Veuiler saisir un nombre (en cm) : "))

Volume = Longueur*Largeur*Hauteur

print(f"Le volume du parallélépipède est de {Volume} centimètre³")